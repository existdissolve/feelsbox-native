const importFeels = async () => {
    await connect();

    const Feel = mongoose.model('Feel', FeelSchema);
    const {feelings} = feels;
    const keys = Object.keys(feelings);
    const categoryMap = {
        misc: '5f95d65ac1e161cb7034b361',
        event: '5ea038409ce0aa3307f74c09',
        food: '5ebbe871a09fe07c1f7b5fca',
        drink: '5ebfd7343d4f91bfad4de84d'
    };

    for (const key of keys) {
        const feel = feelings[key];
        const {category, pixels: oldPixels} = feel;
        const categoryId = categoryMap[category];
        const newPixels = [];

        if (Array.isArray(oldPixels)) {
            let position = -2;

            for (const pixel of oldPixels) {
                position++;

                if (!pixel) {
                    continue;
                }

                const {c: color} = pixel;

                newPixels.push({color, position});
            }
        } else {
            const pixelKeys = Object.keys(oldPixels);

            for (const position of pixelKeys) {
                if (isNaN(position)) {
                    continue;
                }

                const {c: color} = oldPixels[position];

                newPixels.push({color, position: position - 1});
            }
        }

        const payload = {
            active: true,
            ...categoryId && {category: categoryId},
            frames: [{
                active: true,
                pixels: newPixels
            }],
            name: key,
            owner: '5e7fc4eeed6ac73e8f6e01cf'
        };

        await Feel.create(payload);
    }
};

